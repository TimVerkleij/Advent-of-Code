const {PerformanceObserver, performance} = require('perf_hooks');

let t0 = performance.now()

let brackets = {"]": 0, "}": 0, ")": 0, ">": 0}
let bracketValues = [")", "]", "}", ">"]
let output = []

for (let i = 0; i < input.length; i++) {
    const line = input[i];
    let lastOpenedBrackets = []
    let corruptLine = false
    for (let j = 0; j < line.length; j++) {
        const bracket = line[j];

        if (bracket === "(") {
            lastOpenedBrackets.push(")")
        } else if (bracket === "[") {
            lastOpenedBrackets.push("]")
        } else if (bracket === "{") {
            lastOpenedBrackets.push("}")
        } else if (bracket === "<") {
            lastOpenedBrackets.push(">")
        } else {
            if (lastOpenedBrackets[lastOpenedBrackets.length - 1] === bracket) {
                // console.log('good bracket')
                lastOpenedBrackets.pop()
            } else {
                corruptLine = true
                brackets[`${bracket}`]++
                break
            }

        }
    }
    if(!corruptLine) {
        lastOpenedBrackets = lastOpenedBrackets.reverse()
        let total = 0
        for (let j = 0; j < lastOpenedBrackets.length; j++) {
            const bracket = lastOpenedBrackets[j];
            total *= 5
            let value = bracketValues.indexOf(bracket) + 1
            total += value
        }
        output.push(total)
    }

}

let t1 = performance.now()

output = output.sort((a,b) => a - b)
console.log(output[Math.floor(output.length / 2)])
